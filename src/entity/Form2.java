package entity;

public class Form2 {
    public int id;
    public String auth;
    public String auth_time;
    public String department;
    public String deliveryTerm;
    public String deliveryFormName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAuth() {
        return auth;
    }

    public void setAuth(String auth) {
        this.auth = auth;
    }

    public String getAuth_time() {
        return auth_time;
    }

    public void setAuth_time(String auth_time) {
        this.auth_time = auth_time;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getDeliveryTerm() {
        return deliveryTerm;
    }

    public void setDeliveryTerm(String deliveryTerm) {
        this.deliveryTerm = deliveryTerm;
    }

    public String getDeliveryFormName() {
        return deliveryFormName;
    }

    public void setDeliveryFormName(String deliveryFormName) {
        this.deliveryFormName = deliveryFormName;
    }
}
